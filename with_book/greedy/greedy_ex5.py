#아이들에게 1개씩 쿠키를 나눠줘야한다. 각 아이 child_i마다 그리드 팩터 gi를 가지고 있으며 이는 아이가 만족하는 최소 쿠키ㅢ 크기를 말한다 각 쿠키 cookie_j는 크기 sj를 갖고 있으며, sj>=gi이어야 아이가 만족하며 쿠키를 받는다. 최대 몇명의 아이들에게 쿠키를 줄 수 있는지 출력하라
#입력: [1,2,3], [1,1]
#출력: 1

def cookie(g, s):
    # 1. 정렬
    g.sort()
    s.sort()

    i = 0  # 아이 포인터
    j = 0  # 쿠키 포인터
    count = 0

    # 2. 투 포인터 탐색
    while i < len(g) and j < len(s):
        if s[j] >= g[i]:
            # 현재 쿠키로 현재 아이를 만족시킬 수 있음
            count += 1
            i += 1   # 다음 아이
            j += 1   # 다음 쿠키
        else:
            # 쿠키가 너무 작음 → 더 큰 쿠키를 찾아야 함
            j += 1

    return count


# 테스트
print(cookie([1, 2, 3], [1, 1]))
